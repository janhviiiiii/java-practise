public class forLoop {
    public static void main(String[] args) {
        
        for(int a = -15 ; a <= -10; a++)
        {
            System.out.println(a);
        }
        System.out.println("==================");

        for(int a = -5; a >= -10; a--)
        {
            System.out.println(a);
        }
        System.out.println("==================");

        for(int a = 45; a >= 41; a--)
        {
            System.out.println(a);
        }
        System.out.println("==================");

        for(int a = 51; a <= 59; a++)
        {
            System.out.println(a);
        }
        System.out.println("==================");

        for(char a = 'm'; a <= 'p'; a++)
        {
            System.out.println(a);
        }
        System.out.println("==================");

        for(char a = 'E'; a >= 'A'; a--)
        {
            System.out.println(a);
        }
        

    }
    
}
