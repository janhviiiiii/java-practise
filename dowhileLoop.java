public class dowhileLoop {
    public static void main(String[] args) {
        
        int a = 5;
        do{
            System.out.println(a);
            a--;
        }
        while(a>=-5);
        System.out.println("=================");

        int b = -11;
        do{
            System.out.println(b);
            b++;
        }
        while(b<=-1);
        System.out.println("=================");

        int c = 1234;
        do{
            System.out.println(c);
            c--;
        }
        while(c>=1224);
        System.out.println("=================");

        int d = 1111;
        do{
            System.out.println(d);
            d++;
        }
        while(d<=1121);
        System.out.println("=================");

        char e = 'E';
        do{
            System.out.println(e);
            e++;
        }
        while(e<='M');
        System.out.println("=================");
        
        char f = 'p';
        do{
            System.out.println(f);
            f--;
        }
        while(f>='m');

        
    }
    
}
