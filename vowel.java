public class vowel {
    public static void main(String[] args) {
        char a = 'j';
        if(a == 'a' || a =='e' || a =='i' || a =='o' || a == 'u')
        {
            System.out.println(a + " is a vowel ");
        }
        else
        {
            System.out.println(a + " is a consonant");
        }
    }
}
